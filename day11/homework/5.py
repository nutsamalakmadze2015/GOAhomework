# 5)იგივე რამ გააკეთეთ ოღონდ უკვე or-ის გამოყენებით მაგალითად True or False და კომენტარში ახსენით თუ რატომ 
# არის საბოლოო პასუხი True და კიდე ბევრი მაგალითი გააკეთეთ კომენტარით ახსენით
print(False or False)#False იმიტომ რომ არცერერთ პირობას არ ვაკმაყოფილებთ
print(True or False)#False იმიტომ რომ ერთ პირობას არ ვაკმაყოფილებთ
print(True or True)#True იმიტომ რომ ორივე პირობას არ ვაკმაყოფილებთ
print(False or True)#False იმიტომ რომ ერთ პირობას არ ვაკმაყოფილებთ